# Статуи
# Вы получили в подарок на день рождения статуи разных размеров, каждая статуя
# имеет неотрицательный целочисленный размер. Поскольку Вам нравится доводить
# вещи до совершенства, то необходимо расположить их от меньшего к большему,
# чтобы каждая статуя была больше предыдущей ровно на 1. Для этого Вам могут
# понадобиться дополнительные статуи. Определите количество отсутствующих
# статуй.
# Пример
# Для статуй = [6, 2, 3, 8] результат должен быть = 3. Иными словами, у Вас
# отсутствуют статуи размеров 4, 5 и 7.


def missing_statues(arr):
    if not arr:
        return 0  # если список пуст, ничего не нужно добавлять
    min_size = min(arr)
    max_size = max(arr)
    # Общее число статуй, которые должны быть (от min до max включая)
    total_needed = max_size - min_size + 1
    # Количество уже имеющихся статуй
    actual = len(arr)
    # Количество недостающих статуй
    missing = total_needed - actual
    return missing
